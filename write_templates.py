f = open('scheduler/views.py', 'w', encoding='utf-8')
f.write('from django.shortcuts import render, redirect, get_object_or_404\n')
f.write('from django.utils import timezone\n')
f.write('from django.db.models import Q\n')
f.write('from django.contrib.auth.models import User\n')
f.write('from django.contrib import messages\n')
f.write('from .models import Client, Student, StaffMember, Appointment\n')
f.write('from .forms import AppointmentForm, ClientForm, StudentForm\n')
f.write('\n')
f.write('def dashboard(request):\n')
f.write('    today = timezone.now().date()\n')
f.write('    todays = Appointment.objects.filter(date=today)\n')
f.write('    upcoming = Appointment.objects.filter(date__gte=today).order_by("date")[:5]\n')
f.write('    total_clients = Client.objects.count()\n')
f.write('    total_students = Student.objects.count()\n')
f.write('    total_appointments = Appointment.objects.count()\n')
f.write('    context = {\n')
f.write('        "todays": todays,\n')
f.write('        "upcoming": upcoming,\n')
f.write('        "total_clients": total_clients,\n')
f.write('        "total_students": total_students,\n')
f.write('        "total_appointments": total_appointments,\n')
f.write('        "today": today,\n')
f.write('    }\n')
f.write('    return render(request, "scheduler/dashboard.html", context)\n')
f.write('\n')
f.write('def appointments(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_appointments = Appointment.objects.all().order_by("-date")\n')
f.write('    if query:\n')
f.write('        all_appointments = all_appointments.filter(\n')
f.write('            Q(title__icontains=query) | Q(location__icontains=query) |\n')
f.write('            Q(client__name__icontains=query) | Q(student__name__icontains=query)\n')
f.write('        )\n')
f.write('    form = AppointmentForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = AppointmentForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("appointments")\n')
f.write('    return render(request, "scheduler/appointments.html", {"appointments": all_appointments, "form": form, "query": query})\n')
f.write('\n')
f.write('def delete_appointment(request, pk):\n')
f.write('    a = get_object_or_404(Appointment, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        a.delete()\n')
f.write('    return redirect("appointments")\n')
f.write('\n')
f.write('def edit_appointment(request, pk):\n')
f.write('    a = get_object_or_404(Appointment, pk=pk)\n')
f.write('    form = AppointmentForm(instance=a)\n')
f.write('    if request.method == "POST":\n')
f.write('        form = AppointmentForm(request.POST, instance=a)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("appointments")\n')
f.write('    return render(request, "scheduler/edit_appointment.html", {"form": form, "appointment": a})\n')
f.write('\n')
f.write('def clients(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_clients = Client.objects.all().order_by("name")\n')
f.write('    if query:\n')
f.write('        all_clients = all_clients.filter(\n')
f.write('            Q(name__icontains=query) | Q(phone__icontains=query) | Q(email__icontains=query)\n')
f.write('        )\n')
f.write('    form = ClientForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = ClientForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("clients")\n')
f.write('    return render(request, "scheduler/clients.html", {"clients": all_clients, "form": form, "query": query})\n')
f.write('\n')
f.write('def delete_client(request, pk):\n')
f.write('    c = get_object_or_404(Client, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        c.delete()\n')
f.write('    return redirect("clients")\n')
f.write('\n')
f.write('def students(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_students = Student.objects.all().order_by("name")\n')
f.write('    if query:\n')
f.write('        all_students = all_students.filter(\n')
f.write('            Q(name__icontains=query) | Q(phone__icontains=query) | Q(email__icontains=query)\n')
f.write('        )\n')
f.write('    form = StudentForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = StudentForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("students")\n')
f.write('    return render(request, "scheduler/students.html", {"students": all_students, "form": form, "query": query})\n')
f.write('\n')
f.write('def delete_student(request, pk):\n')
f.write('    s = get_object_or_404(Student, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        s.delete()\n')
f.write('    return redirect("students")\n')
f.write('\n')
f.write('def calendar_view(request):\n')
f.write('    appointments = Appointment.objects.all().order_by("date")\n')
f.write('    return render(request, "scheduler/calendar.html", {"appointments": appointments})\n')
f.write('\n')
f.write('def setup(request):\n')
f.write('    if User.objects.filter(is_superuser=True).exists():\n')
f.write('        return redirect("dashboard")\n')
f.write('    if request.method == "POST":\n')
f.write('        key = request.POST.get("key")\n')
f.write('        if key != "vicnice2026":\n')
f.write('            messages.error(request, "Wrong setup key!")\n')
f.write('            return render(request, "scheduler/setup.html")\n')
f.write('        username = request.POST.get("username")\n')
f.write('        email = request.POST.get("email", "")\n')
f.write('        password = request.POST.get("password")\n')
f.write('        User.objects.create_superuser(username=username, email=email, password=password)\n')
f.write('        messages.success(request, "Admin account created! You can now log in.")\n')
f.write('        return redirect("/admin/")\n')
f.write('    return render(request, "scheduler/setup.html")\n')
f.close()
print('views.py done!')