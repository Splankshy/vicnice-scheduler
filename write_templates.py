f.write('      <td>{{ a.date }}</td>\n')
f.write('      <td>{{ a.start_time|default:"-" }}{% if a.end_time %} - {{ a.end_time }}{% endif %}</td>\n')
f.write('      <td>{{ a.client|default:a.student|default:"-" }}</td>\n')
f.write('      <td><span class="badge badge-{{ a.status }}">{{ a.get_status_display }}</span></td>\n')
f.write('      <td>\n')
f.write('        <a href="/appointments/{{ a.pk }}/edit/" class="btn btn-primary" style="font-size:12px;padding:4px 10px;">Edit</a>\n')
f.write('        <form method="post" action="/appointments/{{ a.pk }}/delete/" style="display:inline;">\n')
f.write('          {% csrf_token %}\n')
f.write('          <button type="submit" style="background:#fee2e2;color:#991b1b;border:none;padding:4px 10px;border-radius:6px;font-size:12px;cursor:pointer;" onclick="return confirm(\'Delete?\')">Delete</button>\n')
f.write('        </form>\n')
f.write('      </td>\n')
f.write('    </tr>\n')
f.write('    {% endfor %}\n')
f.write('    </tbody>\n')
f.write('  </table>\n')
f.write('  {% else %}\n')
f.write('  <div class="empty">{% if query %}No results for "{{ query }}"{% else %}No appointments yet.{% endif %}</div>\n')
f.write('  {% endif %}\n')
f.write('</div>\n')
f.write('{% endblock %}\n')
f.close()
