f = open('scheduler/views.py', 'w', encoding='utf-8')
f.write('from django.shortcuts import render, redirect, get_object_or_404\n')
f.write('from django.utils import timezone\n')
f.write('from django.db.models import Q\n')
f.write('from django.contrib.auth.models import User\n')
f.write('from django.contrib.auth import authenticate, login, logout\n')
f.write('from django.contrib import messages\n')
f.write('from django.contrib.auth.decorators import login_required\n')
f.write('from django.core.mail import send_mail\n')
f.write('from django.conf import settings\n')
f.write('from .models import Client, Student, StaffMember, Appointment\n')
f.write('from .forms import AppointmentForm, ClientForm, StudentForm\n')
f.write('from datetime import date\n')
f.write('\n')
f.write('def login_view(request):\n')
f.write('    if request.user.is_authenticated:\n')
f.write('        return redirect("dashboard")\n')
f.write('    if request.method == "POST":\n')
f.write('        username = request.POST.get("username")\n')
f.write('        password = request.POST.get("password")\n')
f.write('        user = authenticate(request, username=username, password=password)\n')
f.write('        if user is not None:\n')
f.write('            login(request, user)\n')
f.write('            return redirect("dashboard")\n')
f.write('        else:\n')
f.write('            messages.error(request, "Invalid username or password")\n')
f.write('    return render(request, "scheduler/login.html")\n')
f.write('\n')
f.write('def logout_view(request):\n')
f.write('    logout(request)\n')
f.write('    return redirect("login")\n')
f.write('\n')
f.write('@login_required\n')
f.write('def dashboard(request):\n')
f.write('    today = timezone.now().date()\n')
f.write('    todays = Appointment.objects.filter(date=today)\n')
f.write('    upcoming = Appointment.objects.filter(date__gte=today).order_by("date")[:5]\n')
f.write('    total_clients = Client.objects.count()\n')
f.write('    total_students = Student.objects.count()\n')
f.write('    total_appointments = Appointment.objects.count()\n')
f.write('    context = {\n')
f.write('        "todays": todays,\n')
f.write('        "upcoming": upcoming,\n')
f.write('        "total_clients": total_clients,\n')
f.write('        "total_students": total_students,\n')
f.write('        "total_appointments": total_appointments,\n')
f.write('        "today": today,\n')
f.write('    }\n')
f.write('    return render(request, "scheduler/dashboard.html", context)\n')
f.write('\n')
f.write('@login_required\n')
f.write('def appointments(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_appointments = Appointment.objects.all().order_by("-date")\n')
f.write('    if query:\n')
f.write('        all_appointments = all_appointments.filter(\n')
f.write('            Q(title__icontains=query) | Q(location__icontains=query) |\n')
f.write('            Q(client__name__icontains=query) | Q(student__name__icontains=query)\n')
f.write('        )\n')
f.write('    form = AppointmentForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = AppointmentForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("appointments")\n')
f.write('    return render(request, "scheduler/appointments.html", {"appointments": all_appointments, "form": form, "query": query})\n')
f.write('\n')
f.write('@login_required\n')
f.write('def delete_appointment(request, pk):\n')
f.write('    a = get_object_or_404(Appointment, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        a.delete()\n')
f.write('    return redirect("appointments")\n')
f.write('\n')
f.write('@login_required\n')
f.write('def edit_appointment(request, pk):\n')
f.write('    a = get_object_or_404(Appointment, pk=pk)\n')
f.write('    form = AppointmentForm(instance=a)\n')
f.write('    if request.method == "POST":\n')
f.write('        form = AppointmentForm(request.POST, instance=a)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("appointments")\n')
f.write('    return render(request, "scheduler/edit_appointment.html", {"form": form, "appointment": a})\n')
f.write('\n')
f.write('@login_required\n')
f.write('def print_appointments(request):\n')
f.write('    appointments = Appointment.objects.all().order_by("date")\n')
f.write('    return render(request, "scheduler/print_appointments.html", {\n')
f.write('        "appointments": appointments,\n')
f.write('        "today": date.today().strftime("%B %d, %Y")\n')
f.write('    })\n')
f.write('\n')
f.write('@login_required\n')
f.write('def send_reminder(request, pk):\n')
f.write('    a = get_object_or_404(Appointment, pk=pk)\n')
f.write('    recipient = None\n')
f.write('    name = None\n')
f.write('    if a.client and a.client.email:\n')
f.write('        recipient = a.client.email\n')
f.write('        name = a.client.name\n')
f.write('    elif a.student and a.student.email:\n')
f.write('        recipient = a.student.email\n')
f.write('        name = a.student.name\n')
f.write('    if recipient:\n')
f.write('        subject = f"Reminder: {a.title} on {a.date}"\n')
f.write('        message = f"Dear {name},\\n\\nThis is a reminder for your upcoming appointment at Vic_nice Home Concepts.\\n\\nAppointment: {a.title}\\nType: {a.get_type_display()}\\nDate: {a.date}\\nTime: {a.start_time or chr(39)}TBD{chr(39)}\\nLocation: {a.location or chr(39)}Vic_nice Studio{chr(39)}\\n\\nPlease contact us if you need to reschedule.\\n\\nBest regards,\\nVic_nice Home Concepts"\n')
f.write('        try:\n')
f.write('            send_mail(subject, message, settings.DEFAULT_FROM_EMAIL, [recipient])\n')
f.write('            messages.success(request, f"Reminder sent to {recipient}!")\n')
f.write('        except Exception as e:\n')
f.write('            messages.error(request, f"Failed to send: {str(e)}")\n')
f.write('    else:\n')
f.write('        messages.error(request, "No email address found for this appointment.")\n')
f.write('    return redirect("appointments")\n')
f.write('\n')
f.write('@login_required\n')
f.write('def clients(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_clients = Client.objects.all().order_by("name")\n')
f.write('    if query:\n')
f.write('        all_clients = all_clients.filter(\n')
f.write('            Q(name__icontains=query) | Q(phone__icontains=query) | Q(email__icontains=query)\n')
f.write('        )\n')
f.write('    form = ClientForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = ClientForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("clients")\n')
f.write('    return render(request, "scheduler/clients.html", {"clients": all_clients, "form": form, "query": query})\n')
f.write('\n')
f.write('@login_required\n')
f.write('def delete_client(request, pk):\n')
f.write('    c = get_object_or_404(Client, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        c.delete()\n')
f.write('    return redirect("clients")\n')
f.write('\n')
f.write('@login_required\n')
f.write('def students(request):\n')
f.write('    query = request.GET.get("q", "")\n')
f.write('    all_students = Student.objects.all().order_by("name")\n')
f.write('    if query:\n')
f.write('        all_students = all_students.filter(\n')
f.write('            Q(name__icontains=query) | Q(phone__icontains=query) | Q(email__icontains=query)\n')
f.write('        )\n')
f.write('    form = StudentForm()\n')
f.write('    if request.method == "POST":\n')
f.write('        form = StudentForm(request.POST)\n')
f.write('        if form.is_valid():\n')
f.write('            form.save()\n')
f.write('            return redirect("students")\n')
f.write('    return render(request, "scheduler/students.html", {"students": all_students, "form": form, "query": query})\n')
f.write('\n')
f.write('@login_required\n')
f.write('def delete_student(request, pk):\n')
f.write('    s = get_object_or_404(Student, pk=pk)\n')
f.write('    if request.method == "POST":\n')
f.write('        s.delete()\n')
f.write('    return redirect("students")\n')
f.write('\n')
f.write('@login_required\n')
f.write('def calendar_view(request):\n')
f.write('    import calendar as cal_module\n')
f.write('    today = date.today()\n')
f.write('    month = int(request.GET.get("month", today.month))\n')
f.write('    year = int(request.GET.get("year", today.year))\n')
f.write('    if month < 1:\n')
f.write('        month = 12\n')
f.write('        year -= 1\n')
f.write('    if month > 12:\n')
f.write('        month = 1\n')
f.write('        year += 1\n')
f.write('    prev_month = month - 1 if month > 1 else 12\n')
f.write('    prev_year = year - 1 if month == 1 else year\n')
f.write('    next_month = month + 1 if month < 12 else 1\n')
f.write('    next_year = year + 1 if month == 12 else year\n')
f.write('    first_weekday, days_in_month = cal_module.monthrange(year, month)\n')
f.write('    first_weekday = (first_weekday + 1) % 7\n')
f.write('    calendar_days = [0] * first_weekday + list(range(1, days_in_month + 1))\n')
f.write('    appointments = Appointment.objects.all().order_by("date")\n')
f.write('    month_appointments = appointments.filter(date__month=month, date__year=year)\n')
f.write('    days_with_events = set(a.date.day for a in month_appointments)\n')
f.write('    context = {\n')
f.write('        "appointments": appointments,\n')
f.write('        "month_appointments": month_appointments,\n')
f.write('        "calendar_days": calendar_days,\n')
f.write('        "month": month,\n')
f.write('        "year": year,\n')
f.write('        "month_name": cal_module.month_name[month],\n')
f.write('        "prev_month": prev_month,\n')
f.write('        "prev_year": prev_year,\n')
f.write('        "next_month": next_month,\n')
f.write('        "next_year": next_year,\n')
f.write('        "today_day": today.day,\n')
f.write('        "today_month": today.month,\n')
f.write('        "today_year": today.year,\n')
f.write('        "days_with_events": days_with_events,\n')
f.write('    }\n')
f.write('    return render(request, "scheduler/calendar.html", context)\n')
f.write('\n')
f.write('def setup(request):\n')
f.write('    if User.objects.filter(is_superuser=True).exists():\n')
f.write('        return redirect("dashboard")\n')
f.write('    if request.method == "POST":\n')
f.write('        key = request.POST.get("key")\n')
f.write('        if key != "vicnice2026":\n')
f.write('            messages.error(request, "Wrong setup key!")\n')
f.write('            return render(request, "scheduler/setup.html")\n')
f.write('        username = request.POST.get("username")\n')
f.write('        email = request.POST.get("email", "")\n')
f.write('        password = request.POST.get("password")\n')
f.write('        User.objects.create_superuser(username=username, email=email, password=password)\n')
f.write('        messages.success(request, "Admin account created! You can now log in.")\n')
f.write('        return redirect("/admin/")\n')
f.write('    return render(request, "scheduler/setup.html")\n')
f.close()
print('views.py complete!')